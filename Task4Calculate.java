package Leetcode;

public class Task4Calculate {   //To display add,sub,mul,div of two numbers
	public static void main(String[] args) {
		int a=120;
		int b=60;
		int c=a+b;
		int d=a-b;
		int e=a*b;
		int f=a/b;
		
		System.out.println("The sum of two numbers are:" +c);
		System.out.println("The subraction of two numbers are:" +d);
		System.out.println("The product of two numbers are:" +e);
		System.out.println("The division of two numbers are:"+f);
	}
}
