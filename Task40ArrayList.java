package Leetcode;
import java.util.ArrayList;
//Java program to create a new array list, add some elements (string) and print out the collection. 

public class Task40ArrayList {
	public static void main(String[] args) {
		
		// creating a new arraylist
		ArrayList<String> collection = new ArrayList<>();
		
		//adding some string elements to the collection
		collection.add("Apple");
		collection.add("Orange");
		collection.add("Mango");
		collection.add("Watermelon");
		
		//printing the collection
		System.out.println("Collection :" + collection);
	}

}
